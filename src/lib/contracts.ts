export function createContractConfig(
  address: Address,
  abi: Abi,
  chainId?: number
) {
  return {
    address,
    abi,
    chainId,
  };
}

export function isValidAddress(address: string): address is Address {
  return /^0x[a-fA-F0-9]{40}$/.test(address);
}

export function formatAddress(address: Address, chars = 4): string {
  return `${address.slice(0, chars + 2)}...${address.slice(-chars)}`;
}

export function createERC20Contract(address: Address, chainId?: number) {
  return createContractConfig(address, ERC20_ABI, chainId);
}